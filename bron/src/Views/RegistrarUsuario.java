    private void registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarActionPerformed
        Usuario u = new Usuario();
        u.setDni_usuario(Integer.parseInt(dni.getText()));
        u.setUsuario(usua.getText());
        u.setContrasena(contrasena.getText());
        u.setNombres(nombres.getText());
        u.setApellidos(apellidos.getText());
        Date date = fechaNac.getDate();
        LocalDate localDate = new java.sql.Date(date.getTime()).toLocalDate();
        u.setFecha_nacimiento(localDate);
        u.setTelefono(telefono.getText());
        u.setCorreo(correo.getText());
        u.setTipo_usuario((String) tipousuario.getSelectedItem());
        usuario = u;
        setVisible(false);
    }//GEN-LAST:event_registrarActionPerformed

    private void cerrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cerrarActionPerformed
        usuario = null;
        setVisible(false);
    }//GEN-LAST:event_cerrarActionPerformed
    
    public void iniciar(Usuario u){
        this.usuario=u;
        dni.setText(String.valueOf(u.getDni_usuario()));
        usua.setText(u.getUsuario());
        contrasena.setText(u.getContrasena());
        nombres.setText(u.getNombres());
        apellidos.setText(u.getApellidos());
        Date date = java.sql.Date.valueOf(u.getFecha_nacimiento());
        fechaNac.setDate(date);
        telefono.setText(u.getTelefono());
        correo.setText(u.getCorreo());
    }
    
